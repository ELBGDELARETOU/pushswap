#include "push_swap.h"

void	ft_stock(int argc, char **argv, t_list **list)
{
	t_list	*new;
	t_list	*current;
	int		i;

	i = 1;
	*list = NULL;
	while (i < argc)
	{
		new = malloc(sizeof(t_list));
		if (!new)
			return ;
		new->content = ft_atoi(argv[i]);
		new->next = NULL;
		if (*list == NULL)
		{
			*list = new;
			current = *list;
		}
		else
		{
			current->next = new;
			current = new;
		}
		i++;
	}
}

int	main(int argc, char **argv)
{
	t_list	*a;
	t_list	*b;

	b = NULL;
	if (argc == 1)
		return (0);
	ft_stock(argc, argv, &a);
	
	// swap(&a);
	push_a(&a, &b);
	// rev_rotate(&a);
	rotate_a(&a);
	printf("a\n");
	while (a)
	{
		printf("%d\n", a->content);
		a = a->next;
	}
	printf("b\n");
	while (b)
	{
		printf("%d", b->content);
		b = b->next;
	}
 }
